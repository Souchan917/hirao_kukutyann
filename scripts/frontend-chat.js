function validateSurvey() {
    const unansweredCategories = [];
    const requiredFields = {
        satisfaction: 'Ê∫ÄË∂≥Â∫¶',
        personalization: 'ÂÄãÂà•Âåñ„Åï„Çå„ÅüÂõûÁ≠î',
        comparison: 'ÊØîËºÉ',
        intention: 'ÊÑèÂõ≥„ÅÆÁêÜËß£',
        age: 'Âπ¥‰ª£',
        gender: 'ÊÄßÂà•',
        occupation: 'ËÅ∑Ê•≠',
        experience: 'ÁµåÈ®ìÂπ¥Êï∞'
    };

    Object.entries(requiredFields).forEach(([key, label]) => {
        if (!state.surveyAnswers[key]) {
            unansweredCategories.push(label);
        }
    });

    return unansweredCategories;
}

function createSurveyData(sessionId) {
    return {
        timestamp: new Date().toISOString(),
        answers: { ...state.surveyAnswers },
        sessionId: sessionId
    };
}

